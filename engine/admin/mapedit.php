<div class="well">
<p>В качестве редактора карт движок RAPTOR использует Tiled. Это замечательный и многофункциональный конструктор мира. <a href='http://www.mapeditor.org/download.html'>Скачать</a></p>
<p>
Работать с ним очень просто!
<li>1. Откройте редактор, загрузите туда нужные вам тайлы</li>
<li>2. Создайте карту, разместив нужные элементы на свой вкус. Сохраните карту в формате JSON. Назвите, например, <b>MAP-1.json</b></li>
<li>3. Откройте созданный файл и пропишите там правильный путь к картинкам тайлов (например: <b>..\/..\/Graphics\/Tilesets\/tileset.png</b>)</li>
<li>4. Загрузите карту в директорию <b>/storage/static/Data/Maps</b>. Имя карты должно быть MAP-X, где Х - любое число</li>
<li>5. Создайте в админке новую локацию и пропишите соответствующий номер JSON-карты</li>
<li>6. Телепортируйтесь в нужную локацию и любуйтесь картой</li>
</p>
</div>
